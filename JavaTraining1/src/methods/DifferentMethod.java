package methods;

public class DifferentMethod {
	public void method1() {
		System.out.println("This is Method1");
	}
	public void method2() {
		System.out.println("This is Method2");
	}
	public static void main(String[] args) {
		DifferentMethod abc = new DifferentMethod();
		abc.method1();
		abc.method2();
	}

}
